let wynik = 85;

switch (true) {
    case (wynik >= 90):
        console.log("Bardzo dobry");
        break;
    case (wynik >= 80):
        console.log("Dobry");
        break;
    case (wynik >= 70):
        console.log("Dostateczny");
        break;
    case (wynik >= 60):
        console.log("Mierny");
        break;
    default:
        console.log("Niezaliczony");
}
